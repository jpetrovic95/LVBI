/**
 * @brief Send a command byte to the SSD1306 controller.
 *
 * @details
 *   Writes a single command byte over I2C to the OLED controller.
 *
 * @param[in] cmd Command byte to send.
 * @return ESP_OK on success, ESP_ERR_* on failure.
 */
static esp_err_t oled_ssd1306_send_command(uint8_t cmd);

esp_err_t oled_ssd1306_init(void) {
    /* Register device (harmless if already done; will log warning) */
    hwa_i2c_register_device("oled", OLED_ADDR);

    const uint8_t seq[] = {
        0xAE,       /* display off */
        0xD5, 0x80, /* clock divide */
        0xA8, 0x1F, /* multiplex for 128x32 (0x1F) */
        0xD3, 0x00, /* display offset */
        0x40,       /* start line */
        0x8D, 0x14, /* charge pump on */
        0x20, 0x00, /* horizontal addressing mode */
        0xA1,       /* segment remap */
        0xC8,       /* COM scan dec */
        0xDA, 0x02, /* COM pins cfg for 128x32 */
        0x81, 0x8F, /* contrast */
        0xD9, 0xF1, /* pre-charge */
        0xDB, 0x40, /* VCOM detect (0x40 ~ 0.77*Vcc) */
        0xA4,       /* display resume */
        0xA6,       /* normal (not inverted) */
        0xAF        /* display on */
    };

    for (size_t i = 0; i < sizeof(seq); ++i) {
        esp_err_t e = oled_ssd1306_send_command(seq[i]);
        if (e != ESP_OK) {
            ESP_LOGE(TAG, "Init command 0x%02X failed (%s)", seq[i], esp_err_to_name(e));
            if (++s_oled_fail_streak >= 2) {
                ESP_LOGE(TAG, "OLED I2C write failed (init seq)");
            }
            return e;
        }
    }
    ESP_LOGI(TAG, "OLED initialized");
    s_oled_fail_streak = 0;
    /* clear local failure streak on successful init */
    s_oled_fail_streak = 0;
    return ESP_OK;
}

void oled_ssd1306_clear(void) {

	uint8_t line[OLED_WIDTH + 1u] = {0x40}; // 0x40 is the data prefix for I2C
	for (uint8_t page = 0; page < OLED_PAGES; page++) {
		oled_ssd1306_send_command(0xB0 + page); // Set page address
		oled_ssd1306_send_command(0x00);		   // Column low address
		oled_ssd1306_send_command(0x10);		   // Column high address

		memset(&line[1u], 0x00, sizeof(line) - 1u); // Clear the line buffer
        // Write the cleared line to the OLED
        if (hwa_i2c_write("oled", line, sizeof(line)) != ESP_OK) {
            if (++s_oled_fail_streak >= 2) ESP_LOGE(TAG, "OLED draw: consecutive I2C errors (%d)", s_oled_fail_streak);
    } else { s_oled_fail_streak = 0; }
	}
}

void oled_ssd1306_draw_text(uint8_t x, uint8_t y, const char *text) {
	while (*text) {
		char c = *text++;
        if (c < 32 || c > 127) {
            c = 32; // Replace unsupported characters with space
            ESP_LOGW(TAG, "Unsupported character '%c' at position %d, replaced with space", c, *text - 1);
        }

		const uint8_t *glyph = font6x8[(uint8_t)c];
		if (!glyph) {
			text++;
			x += 6;	  // Move to next character position
			continue; // Skip if no glyph found
		}

		// Set page address
		oled_ssd1306_send_command(0xB0 + y);				 // Set page address
		oled_ssd1306_send_command(0x00 + (x & 0x0F));		 // Set low column address
		oled_ssd1306_send_command(0x10 + ((x >> 4) & 0x0F)); // Set high column address

		// Prepare 6-byte glyph + 1 control byte (0x40 for data stream)
		uint8_t data[7] = {0x40}; // First byte = control byte for data
		memcpy(&data[1], glyph, 6);

        if (hwa_i2c_write("oled", data, sizeof(data)) != ESP_OK) {
            if (++s_oled_fail_streak >= 2) ESP_LOGE(TAG, "OLED clear: consecutive I2C errors (%d)", s_oled_fail_streak);
    } else { s_oled_fail_streak = 0; }

		x += 6; // Move to next character position
		//text++;
	}
}

void oled_ssd1306_sleep(bool enable)
{
    uint8_t cmd[3];
    if (enable) {
        cmd[0] = 0x00;   /* control byte: command stream          */
        cmd[1] = 0xAE;   /* DISPLAY_OFF                           */
        cmd[2] = 0x8D;   /* CHARGE_PUMP                           */
    if (hwa_i2c_write("oled", cmd, 2) != ESP_OK) { if (++s_oled_fail_streak >= 2) ESP_LOGE(TAG, "OLED sleep cmd failed"); }          /* 0xAE only      */
        cmd[1] = 0x8D; cmd[2] = 0x10;            /* disable pump   */
    if (hwa_i2c_write("oled", cmd, 3) != ESP_OK) { if (++s_oled_fail_streak >= 2) ESP_LOGE(TAG, "OLED sleep seq step failed"); }
    } else {
        cmd[0] = 0x00;
        cmd[1] = 0x8D; cmd[2] = 0x14;            /* enable pump    */
    if (hwa_i2c_write("oled", cmd, 3) != ESP_OK) { if (++s_oled_fail_streak >= 2) ESP_LOGE(TAG, "OLED wake seq step failed"); }
        cmd[1] = 0xAF;                           /* DISPLAY_ON     */
    if (hwa_i2c_write("oled", cmd, 2) != ESP_OK) { if (++s_oled_fail_streak >= 2) ESP_LOGE(TAG, "OLED sleep state cmd failed"); }
    }
}

static esp_err_t oled_ssd1306_send_command(uint8_t cmd) {
	/*  Control byte for i2c
		Co : bit 8 : Continuation Bit
		* 1 = no-continuation (only one byte to follow)
		* 0 = the controller should expect a stream of bytes.
		D/C# : bit 7 : Data/Command Select bit
		* 1 = the next byte or byte stream will be Data.
		* 0 = a Command byte or byte stream will be coming up next.
		Bits 6-0 will be all zeros.
		Usage:
		0x80 : Single Command byte
		0x00 : Command Stream
		0xC0 : Single Data byte
		0x40 : Data Stream
	*/
    uint8_t buf[2] = {0x00, cmd};
    esp_err_t e = hwa_i2c_write("oled", buf, 2);
    if (e != ESP_OK) { if (++s_oled_fail_streak >= 2) ESP_LOGE(TAG, "OLED wake write failed"); }
    else { s_oled_fail_streak = 0; }
    return e;
}